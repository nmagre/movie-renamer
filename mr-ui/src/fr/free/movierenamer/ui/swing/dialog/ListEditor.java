    public ListEditor(MovieRenamer mr, List<Object> objectList, Class<? extends IEventListener> sendEventTo) {
        this(mr, objectList, sendEventTo, true);
    }

    public ListEditor(MovieRenamer mr, List<Object> objectList, Class<? extends IEventListener> sendEventTo, boolean moveBtn) {
        super(mr, i18n.getLanguageKey("dialog.editor", false));
        this.objectList = objectList;
        this.sendEventTo = sendEventTo;
        initComponents();

        if (!moveBtn) {
            upBtn.setVisible(false);
            downBtn.setVisible(false);
        }

        list.setModel(listModel);
        list.addListSelectionListener(createListListener());

        for (Object obj : objectList) {
            listModel.addElement(obj.toString());
        }

        if (!listModel.isEmpty()) {
            list.setSelectedIndex(0);
        }
    }

    private ListSelectionListener createListListener() {
        return new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                int index = list.getSelectedIndex();
                if (!lse.getValueIsAdjusting()) {
                    removeBtn.setEnabled(index != -1);
                    upBtn.setEnabled(index > 0);
                    downBtn.setEnabled(listModel.size() > 1 && index != -1 && index != listModel.size() - 1);
                }
            }
        };
    }

