    private void applyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyBtnActionPerformed
        List<Object> values = new ArrayList<>();
        ListModel model = list.getModel();
        int size = model.getSize();
        for (int i = 0; i < size; i++) {
            values.add(model.getElementAt(i));
        }

        UIEvent.fireUIEvent(UIEvent.Event.EDITED, sendEventTo, null, objectList, values);
        dispose();
    }//GEN-LAST:event_applyBtnActionPerformed

    private void downBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_downBtnActionPerformed
        int index = list.getSelectedIndex();
        if (index != -1 && index < listModel.size() - 1) {
            Object obj = listModel.get(index);
            listModel.remove(index);
            listModel.add(++index, obj);
            list.setSelectedIndex(index);
        }
    }//GEN-LAST:event_downBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        int index = list.getSelectedIndex();
        if (index != -1) {
            listModel.remove(index);
            if (!listModel.isEmpty()) {
                list.setSelectedIndex(listModel.size() - 1 >= index ? index : index - 1);
            }
        }
    }//GEN-LAST:event_removeBtnActionPerformed

    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        T obj = createNewValue();

        if (obj != null && !obj.toString().isEmpty()) {
            listModel.addElement(obj);
            if (list.getSelectedIndex() == -1) {
                list.setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

